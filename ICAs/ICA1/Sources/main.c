#define RED_LED 0b10000000
#define UP_BUTTON 0b00010000
#define INDEX_MAX 10
/********************************************************************/
// Local Prototypes
/********************************************************************/

/********************************************************************/
// Global Variables
/********************************************************************/
unsigned int counter, index;

/********************************************************************/
// Constants
/********************************************************************/

/********************************************************************/
// Main Entry
/********************************************************************/
void main(void)
{
  //Any main local variables must be declared here
  //
  PT1AD1 |= RED_LED; 
  DDR1AD1 |= RED_LED;
  counter = 0;
  index = 0;

  // main entry point
  _DISABLE_COP();
  //EnableInterrupts;
  
/********************************************************************/
  // one-time initializations
/********************************************************************/


/********************************************************************/
  // main program loop
/********************************************************************/

  for (;;)
  {
    if(++counter == 0)
    {
      if((++index >= INDEX_MAX))
      {
        index = 0; 
        PT1AD1 ^= RED_LED;
      }
    }
  }                   
}

